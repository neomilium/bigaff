/* pointeur vers la position courante d'écriture dans le framebuffer */
static volatile uint8_t *_framebuffer = _framebuffer1;
static volatile uint8_t *_fb_ptr;
static volatile uint8_t *_fb_end;

void
framebuffer_init (void)
{
  framebuffer_swap();
}

// Get display buffer 
// Note: _framebuffer is the current draft framebuffer
volatile uint8_t*
framebuffer_get_display (void)
{
  if (_framebuffer == _framebuffer2) {
    return _framebuffer1;
  } else {
    return _framebuffer2;
  }
}

void
framebuffer_swap (void)
{
  if (_framebuffer == _framebuffer2) {
    _framebuffer = _framebuffer1;
    _fb_ptr = _framebuffer1;        // initialise le pointeur fb_ptr au début du buffer d'affichage (framebuffer)
    _fb_end = _framebuffer1 + sizeof (uint8_t) * FRAMEBUFFER_WIDTH;;
  } else {
    _framebuffer = _framebuffer2;
    _fb_ptr = _framebuffer2;        // initialise le pointeur fb_ptr au début du buffer d'affichage (framebuffer)
    _fb_end = _framebuffer2 + sizeof (uint8_t) * FRAMEBUFFER_WIDTH;;
  }
}

#define MIN(a, b)  (((a) < (b)) ? (a) : (b))
#define MAX(a, b)  (((a) > (b)) ? (a) : (b))
void
framebuffer_display_char (const char c)
{
  size_t s = MIN(5, _fb_end - _fb_ptr);

  memcpy_P ((void *) _fb_ptr, ascii_table[c - 32], s);
  _fb_ptr += s;
  
  if(_fb_end > _fb_ptr) {
    _fb_ptr[0] = 0x00;             // Put an empty column
    _fb_ptr++;
  }
}

void
framebuffer_display_partial_char (const char c, const uint8_t offset)
{
  size_t s = MIN(5 - offset, _fb_end - _fb_ptr);
  memcpy_P ((void *) _fb_ptr, ascii_table[c - 32] + offset, s);
  _fb_ptr += s;
  
  if(_fb_end > _fb_ptr) {
    _fb_ptr[0] = 0x00;             // Put an empty column
    _fb_ptr++;
  }
}

void
framebuffer_display_string_P (const char *string)
{
  char    c;
  while ((c = pgm_read_byte (string++))) {
    framebuffer_display_char (c);
  }
}

void
framebuffer_display_string (const char *string)
{
  char    c;
  while ((c = *(string++)) && (_fb_end > _fb_ptr)) {
    framebuffer_display_char (c);
  }
}

void
framebuffer_finish_line (void)
{
  for (; _fb_ptr < _fb_end; _fb_ptr++) {
    *_fb_ptr = 0x00;           // Put an empty column
  }
  _fb_ptr = _framebuffer;
}

void
framebuffer_blank_column (const uint8_t columns)
{
  for (uint8_t c = 0; c < columns; c++) {
    _fb_ptr[0] = 0x00;           // Put an empty column
    _fb_ptr++;
  }
}

void
framebuffer_display_string_center (const char *string)
{
  size_t  length = strlen (string) * 6;
  if(FRAMEBUFFER_WIDTH >= length ) {
    framebuffer_blank_column (((FRAMEBUFFER_WIDTH - length) / 2));
  }
  framebuffer_display_string (string);
  framebuffer_finish_line ();
}

